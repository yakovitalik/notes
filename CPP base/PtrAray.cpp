// массив указателей на строки

#include <iostream>
using namespace std;
const int DAYS = 7; 
//////////////////////////////////////////////////////////////////////////////
int main()
{
	// массив указателей на строки
	char* arrptrs[DAYS] = 	{ "Понедельник", "Вторник", "Среда", "Четверг", 
		"Пятница", "Суббота", "Воскресенье" };

	// вывод всех строк на экран
	for (int j = 0; j < DAYS; j++)
		cout << arrptrs[j] << endl;

	return 0;
}