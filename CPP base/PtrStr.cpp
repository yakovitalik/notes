// показ строки, определеной через указатель
	
#include <iostream>
using namespace std;

int main()
{	
	void dispstr(char*);		// прототип функции
	char str[] = "У бездельников всегда есть свободное время";
	dispstr(str);
		
	return 0;
}
//////////////////////////////////////////////////////////////////////////////
void dispstr(char* ps)
{
	while(*ps)			// пока не встретим конец строки
		cout << *ps++;		// будем произвольно выводить ее на экран
	cout << endl;
}