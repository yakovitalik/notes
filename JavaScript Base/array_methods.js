/////////////////////////////////////////////////////////////////////
// forEach()
// Проходит по массиву
/////////////////////////////////////////////////////////////////////
let data = [1, 2, 3, 4, 5], sum = 0;

// Вычислить сумму элементов массива
data.forEach(value => {sum += value;});			// sum = 15;

// Инкрементировать каждый элемент массива
data.forEach(function(v, i, a) { a[i] = v + 1; });	// data = [2, 3, 4, 5, 6]


/////////////////////////////////////////////////////////////////////
// map()
/////////////////////////////////////////////////////////////////////

let a = [1, 2, 3];
a.map(x => x*x);		// => [1, 4, 9]

/////////////////////////////////////////////////////////////////////
// filter()
/////////////////////////////////////////////////////////////////////

let b = [5, 4, 3, 2, 1];
b.filter(x => x < 3);				// => [2, 1]; значения меньше 3
b.filter((x, i) => i%2 === 0);		// => [5, 3, 1]; все остальные значения